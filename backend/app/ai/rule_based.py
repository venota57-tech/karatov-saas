def classify_review(text: str | None = None, rating: int | None = None, pros: str | None = None, cons: str | None = None) -> dict:
    t = normalize_text(text, pros, cons)
    tags = extract_tags(text, pros, cons)
    has_clear_complaint = _has_any(t, CLEAR_COMPLAINT_WORDS)
    has_soft_note = _has_any(t, SOFT_NOTE_WORDS)

    # Главное правило для ответов: высокая оценка + отсутствие явной претензии = позитив.
    # Детали вроде “тонкое”, “нежное”, “коробочка”, “плотное” остаются тегами, но не превращают отзыв в жалобу.
    category = 'позитив' if rating and rating >= 4 else 'другое'
    if not (rating and rating >= 5 and not has_clear_complaint):
        for cat, words in CATEGORY_KEYWORDS:
            if any(w in t for w in words):
                category = cat
                break
    if rating and rating >= 5 and not has_clear_complaint:
        category = 'позитив'
    elif rating and rating == 4 and not has_clear_complaint and has_soft_note:
        category = 'позитив с замечанием'
    if 'отказ' in t:
        category = 'отказ/возврат'

    if rating is not None and rating <= 3:
        sentiment = 'negative'
    elif has_clear_complaint:
        sentiment = 'negative' if (rating or 0) <= 4 else 'mixed'
    elif (rating is not None and rating >= 4) or any(w in t for w in POSITIVE_WORDS):
        sentiment = 'positive'
    else:
        sentiment = 'neutral'

    if category in {'проба/маркировка', 'камень/вставка'} or (rating is not None and rating <= 2):
        risk = 'high'
    elif category in RISKY_CATEGORIES or sentiment == 'negative':
        risk = 'medium'
    else:
        risk = 'low'

    return {
        'category': category, 'sentiment': sentiment, 'risk_level': risk,
        'can_autopublish': sentiment == 'positive' and risk == 'low' and (rating or 0) >= 5,
        'tags': tags,
        'reason': 'Классификация выполнена локальными правилами: высокая оценка без явной претензии не превращается в негативный сценарий.',
    }

def classify_question(text: str | None = None) -> dict:
    t = normalize_text(text)
    category = 'вопрос_по_характеристикам'
    for cat, words in CATEGORY_KEYWORDS:
        if any(w in t for w in words):
            category = cat
            break
    risk = 'medium' if category in RISKY_CATEGORIES else 'low'
    return {'category': category, 'risk_level': risk, 'can_autopublish': False, 'tags': extract_tags(text), 'reason': 'Классификация вопроса выполнена локальными правилами без обращения к OpenAI.'}

SIGNATURE = "\nС уважением, команда KARATOV"


def _variant(payload: dict, count: int) -> int:
    seed = payload.get('variation_seed')
    try:
        return int(seed) % count
    except Exception:
        return 0


def _sign(text: str) -> str:
    text = (text or '').strip()
    if 'команда karatov' in text.lower():
        return text
    return text + SIGNATURE


def _choose(payload: dict, options: list[str]) -> str:
    return _sign(options[_variant(payload, len(options))])


def _contains_positive_detail(t: str) -> str | None:
    if 'кольц' in t: return 'колечко'
    if 'цепоч' in t or 'цепь' in t: return 'цепочка'
    if 'браслет' in t: return 'браслет'
    if 'сереж' in t or 'серьг' in t: return 'серьги'
    if 'подвес' in t or 'кулон' in t: return 'подвеска'
    return None


